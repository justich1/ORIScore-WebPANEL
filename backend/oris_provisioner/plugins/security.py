def _run_ufw_rule(payload: dict) -> tuple[int, str]:
    action = str(payload.get("action", "allow")).lower().strip()
    direction = str(payload.get("dir", "in")).lower().strip()
    port = str(payload.get("port", "")).strip()
    proto = str(payload.get("proto", "tcp")).lower().strip()
    source = str(payload.get("from", payload.get("source", ""))).strip()
    toip = str(payload.get("toip", "")).strip()
    comment = str(payload.get("comment", "")).strip()[:60]
    if action not in {"allow", "deny", "reject", "limit"}:
        raise RuntimeError("Neplatná UFW akce.")
    if direction not in {"in", "out"}:
        raise RuntimeError("Neplatný směr UFW pravidla.")
    if proto not in {"tcp", "udp", "any"}:
        raise RuntimeError("Neplatný protokol.")
    if not valid_port(port):
        raise RuntimeError("Neplatný port.")
    if source and not valid_ip_or_cidr(source):
        raise RuntimeError("Neplatná FROM IP/CIDR.")
    if toip and not valid_ip_or_cidr(toip):
        raise RuntimeError("Neplatná TO IP/CIDR.")
    cmd = ["ufw"]
    if direction == "out":
        cmd += [action, "out"]
        if toip:
            cmd += ["to", toip, "port", port]
            if proto != "any":
                cmd += ["proto", proto]
        else:
            cmd += [port if proto == "any" else f"{port}/{proto}"]
    else:
        cmd += [action]
        if source:
            cmd += ["from", source, "to", "any", "port", port]
            if proto != "any":
                cmd += ["proto", proto]
        else:
            cmd += [port if proto == "any" else f"{port}/{proto}"]
    if comment:
        cmd += ["comment", comment]
    return run(cmd, check=False)


def handle(ctx: Ctx, job: dict) -> None:
    job_id = int(job["id"])
    typ = str(job["type"])
    payload = payload_dict(job)

    if typ == "security_apply":
        ensure_security_files(ctx)
        write_phpmyadmin_snippet(ctx)
        write_fail2ban_config(ctx)
        run(["systemctl", "restart", "fail2ban"], check=False)
        from .panel import write_panel_vhost
        write_panel_vhost(ctx)
        ctx.nginx_reload_checked()
        ctx.job_log(job_id, "Security Center konfigurace aplikována: Nginx limity, phpMyAdmin ochrana, admin allowlist, Fail2ban.")
        return

    if typ == "firewall_block_ip":
        ctx.job_log(job_id, firewall_block(str(payload.get("ip", "")).strip()))
        return
    if typ == "firewall_unblock_ip":
        ctx.job_log(job_id, firewall_unblock(str(payload.get("ip", "")).strip()))
        return
    if typ == "fail2ban_apply_panel_config":
        write_fail2ban_filters()
        j = payload.get("jails") or {}
        ctx.set_setting("security_fail2ban_maxretry", str(payload.get("maxretry", 5)))
        ctx.set_setting("security_fail2ban_findtime", str(payload.get("findtime", 600)))
        ctx.set_setting("security_fail2ban_bantime", str(payload.get("bantime", 3600)))
        ctx.set_setting("security_fail2ban_phpmyadmin_enabled", "1" if truthy(j.get("oris-nginx-phpmyadmin", "1")) else "0")
        ctx.set_setting("security_fail2ban_badbots_enabled", "1" if truthy(j.get("oris-nginx-badbots", "1")) else "0")
        ctx.set_setting("security_fail2ban_recidive_enabled", "1" if truthy(j.get("recidive", "1")) else "0")
        ctx.set_setting("security_fail2ban_oris_perm_enabled", "1" if truthy(j.get("oris-perm", "1")) else "0")
        ctx.set_setting("security_fail2ban_recidive_maxretry", str(payload.get("recidive_maxretry", 5)))
        ctx.set_setting("security_fail2ban_recidive_findtime", f2b_time_value(payload.get("recidive_findtime", "7d"), "7d"))
        ctx.set_setting("security_fail2ban_recidive_bantime", f2b_time_value(payload.get("recidive_bantime", "-1"), "-1"))
        ctx.set_setting("security_fail2ban_perm_bantime", f2b_time_value(payload.get("perm_bantime", "-1"), "-1"))
        atomic_write("/etc/fail2ban/jail.d/oris-panel.conf", _build_panel_f2b(payload))
        rc, out = run(["systemctl", "restart", "fail2ban"], check=False)
        ctx.job_log(job_id, out.strip() or f"fail2ban restart exit={rc}")
        if rc != 0:
            raise RuntimeError(out)
        return
    if typ == "ufw_enable":
        rc, out = run(["ufw", "--force", "enable"], check=False)
        ctx.job_log(job_id, out.strip() or f"ufw enable exit={rc}")
        if rc != 0: raise RuntimeError(out)
        return
    if typ == "ufw_disable":
        rc, out = run(["ufw", "disable"], check=False)
        ctx.job_log(job_id, out.strip() or f"ufw disable exit={rc}")
        if rc != 0: raise RuntimeError(out)
        return
    if typ == "ufw_reload":
        rc, out = run(["ufw", "reload"], check=False)
        ctx.job_log(job_id, out.strip() or f"ufw reload exit={rc}")
        if rc != 0: raise RuntimeError(out)
        return
    if typ == "ufw_default":
        incoming = str(payload.get("in", payload.get("def_in", "deny"))).lower().strip()
        outgoing = str(payload.get("out", payload.get("def_out", "allow"))).lower().strip()
        if incoming not in {"allow", "deny", "reject"} or outgoing not in {"allow", "deny", "reject"}:
            raise RuntimeError("Neplatná default politika.")
        rc1, out1 = run(["ufw", "default", incoming, "incoming"], check=False)
        rc2, out2 = run(["ufw", "default", outgoing, "outgoing"], check=False)
        ctx.job_log(job_id, (out1 + out2).strip() or "Default politika nastavena.")
        if rc1 != 0 or rc2 != 0: raise RuntimeError((out1 + out2).strip())
        return
    if typ == "ufw_allow":
        rc, out = _run_ufw_rule({"action": "allow", "dir": "in", "port": payload.get("port", ""), "proto": payload.get("proto", "tcp"), "from": payload.get("source", "")})
        ctx.job_log(job_id, out.strip() or "UFW pravidlo přidáno.")
        if rc != 0: raise RuntimeError(out)
        return
    if typ == "ufw_rule":
        rc, out = _run_ufw_rule(payload)
        ctx.job_log(job_id, out.strip() or "UFW pravidlo přidáno.")
        if rc != 0: raise RuntimeError(out)
        return
    if typ == "ufw_apply_ports":
        selected = payload.get("ports") or []

        if not isinstance(selected, list) or not selected:
            raise RuntimeError("Nic nebylo vybráno.")

        lan_net = str(payload.get("lan_net", "192.168.0.0/16")).strip()
        scope = str(payload.get("scope", "auto")).strip().lower()

        if scope not in {"auto", "public", "lan"}:
            scope = "auto"

        if lan_net and not valid_ip_or_cidr(lan_net):
            raise RuntimeError(f"Neplatný LAN rozsah: {lan_net}")

        any_err = False
        added = 0
        skipped = 0

        for token in selected:
            parsed = parse_port_token(str(token))

            if not parsed:
                skipped += 1
                ctx.job_log(job_id, f"SKIP neplatný token portu: {token}")
                continue

            proto, port, klass = parsed

            if klass == "localhost":
                skipped += 1
                ctx.job_log(job_id, f"SKIP localhost port {proto}/{port}")
                continue

            final_scope = scope if scope in {"public", "lan"} else ("lan" if klass == "lan" else "public")

            rule_payload = {
                "action": "allow",
                "dir": "in",
                "port": port,
                "proto": proto,
                "comment": f"ORIS auto {klass} {proto}/{port}",
            }

            if final_scope == "lan":
                rule_payload["from"] = lan_net

            rc, out = _run_ufw_rule(rule_payload)

            ctx.job_log(
                job_id,
                f"{proto}/{port} {final_scope}: " + (out.strip() or f"exit={rc}")
            )

            if rc != 0:
                any_err = True
            else:
                added += 1

        run(["ufw", "reload"], check=False)

        ctx.job_log(job_id, f"UFW auto porty hotovo. Přidáno: {added}, přeskočeno: {skipped}")

        if any_err:
            raise RuntimeError("Některá UFW pravidla se nepodařilo přidat. Viz log jobu.")

        return
    if typ == "ufw_delete":
        num = str(payload.get("num", "")).strip()
        if not num.isdigit() or int(num) < 1: raise RuntimeError("Neplatné číslo UFW pravidla.")
        rc, out = run(["ufw", "--force", "delete", num], check=False)
        ctx.job_log(job_id, out.strip() or "UFW pravidlo smazáno.")
        if rc != 0: raise RuntimeError(out)
        return
    if typ == "fail2ban_reload":
        rc, out = run(["systemctl", "restart", "fail2ban"], check=False)
        ctx.job_log(job_id, out.strip() or f"fail2ban restart exit={rc}")
        if rc != 0: raise RuntimeError(out)
        return
    if typ in {"fail2ban_ban_ip", "fail2ban_unban_ip"}:
        jail = str(payload.get("jail", "sshd")).strip()
        ip = str(payload.get("ip", "")).strip()
        if not _valid_jail(jail): raise RuntimeError("Neplatný jail.")
        if not valid_ip_or_cidr(ip) or "/" in ip: raise RuntimeError("Neplatná IP adresa.")
        action = "banip" if typ == "fail2ban_ban_ip" else "unbanip"
        rc, out = run(["fail2ban-client", "set", jail, action, ip], check=False)
