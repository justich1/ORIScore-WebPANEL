def truthy(v: Any) -> bool:
    return str(v or "").strip().lower() in {"1", "true", "yes", "on", "ano"}


def int_setting(ctx: Ctx, key: str, default: int, lo: int, hi: int) -> int:
    try:
        n = int(str(ctx.setting(key, str(default))).strip())
    except Exception:
        n = default
    return max(lo, min(hi, n))


def f2b_time_value(value: Any, default: str = "3600") -> str:
    raw = str(value if value is not None else default).strip().lower()
    if raw == "-1":
        return "-1"
    # Fail2ban bere čisté sekundy i zkratky typu 10m, 24h, 7d, 1w.
    if re.match(r"^[1-9][0-9]*(?:s|m|h|d|w)?$", raw):
        return raw
    return default


def rate_setting(ctx: Ctx) -> str:
    raw = str(ctx.setting("security_nginx_rate", "10r/s")).strip().lower().replace(" ", "")
    if re.match(r"^[1-9][0-9]*(r/s|r/m)$", raw):
        return raw
    if raw.isdigit() and int(raw) > 0:
        return raw + "r/s"
    return "10r/s"


def ip_lines(text: Any) -> list[str]:
    out: list[str] = []
    for line in str(text or "").replace(",", "\n").splitlines():
        item = line.strip()
        if not item or item.startswith("#"):
            continue
        try:
            if "/" in item:
                ipaddress.ip_network(item, strict=False)
            else:
                ipaddress.ip_address(item)
            out.append(item)
        except Exception:
            continue
    return out


def ensure_security_files(ctx: Ctx | None = None) -> None:
    rate = "10r/s"
    burst = 30
    conn = 30
    enabled = False
    if ctx is not None:
        rate = rate_setting(ctx)
        burst = int_setting(ctx, "security_nginx_burst", 30, 1, 10000)
        conn = int_setting(ctx, "security_nginx_conn", 30, 1, 10000)
        enabled = truthy(ctx.setting("security_nginx_rate_enabled", "0"))

    Path("/etc/nginx/conf.d").mkdir(parents=True, exist_ok=True)
    Path("/etc/nginx/snippets").mkdir(parents=True, exist_ok=True)

    atomic_write("/etc/nginx/conf.d/oris-security-zones.conf", f"""# ORIS Security Center - global zones
# Generated by ORIS provisioner. Do not edit manually.
limit_req_zone $binary_remote_addr zone=oris_req:20m rate={rate};
limit_conn_zone $binary_remote_addr zone=oris_conn:20m;
""")

    if enabled:
        body = f"""# ORIS Security Center - vhost rate limits
limit_req zone=oris_req burst={burst} nodelay;
limit_conn oris_conn {conn};
"""
    else:
        body = "# ORIS Security Center - rate limit disabled\n"
    atomic_write("/etc/nginx/snippets/oris-rate-limit.conf", body)


def access_lines(ips: list[str], indent: str = "    ") -> str:
    if not ips:
        return ""
    return "\n".join(f"{indent}allow {ip};" for ip in ips) + f"\n{indent}deny all;\n"


def write_phpmyadmin_snippet(ctx: Ctx) -> None:
    Path("/etc/nginx/snippets").mkdir(parents=True, exist_ok=True)
    rate_line = ""
    if truthy(ctx.setting("security_phpmyadmin_rate_enabled", "1")):
        rate_line = "    include snippets/oris-rate-limit.conf;\n"
    allow = ""
    if truthy(ctx.setting("security_phpmyadmin_allowlist_enabled", "0")):
        allow = access_lines(ip_lines(ctx.setting("security_phpmyadmin_allowlist", "")), "    ")
    guard = rate_line + allow
    atomic_write("/etc/nginx/snippets/phpmyadmin.conf", f"""# ORIS phpMyAdmin snippet
# Generated by ORIS Security Center / installer.
location = /phpmyadmin {{
    return 301 /phpmyadmin/;
}}

location /phpmyadmin/ {{
{guard}    alias /usr/share/phpmyadmin/;
    index index.php index.html;
    try_files $uri $uri/ /phpmyadmin/index.php?$query_string;
}}

location ~ ^/phpmyadmin/(.+\\.php)$ {{
{guard}    alias /usr/share/phpmyadmin/$1;
    include fastcgi_params;
    fastcgi_param SCRIPT_FILENAME /usr/share/phpmyadmin/$1;
    fastcgi_param SCRIPT_NAME /phpmyadmin/$1;
    fastcgi_param DOCUMENT_ROOT /usr/share/phpmyadmin;
    fastcgi_pass unix:/run/php/php-fpm.sock;
}}

location ~* ^/phpmyadmin/(.+\\.(jpg|jpeg|gif|css|png|js|ico|html|xml|txt|svg|woff|woff2|ttf|map))$ {{
{allow}    alias /usr/share/phpmyadmin/$1;
    expires 7d;
    access_log off;
}}
""")


