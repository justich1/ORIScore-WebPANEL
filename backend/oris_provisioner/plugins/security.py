def valid_port(p: str) -> bool:
    if ":" in p:
        a, b = p.split(":", 1)
        return a.isdigit() and b.isdigit() and 1 <= int(a) <= int(b) <= 65535
    return p.isdigit() and 1 <= int(p) <= 65535

def normalize_port(p: str) -> str:
    return str(p or "").strip().replace("-", ":")


def parse_port_token(token: str) -> tuple[str, str, str] | None:
    """
    Podporuje:
      tcp:21:public
      tcp:40000-40100:public
      tcp:40000:40100:public
    Vrací: proto, port_pro_ufw, scope
    """
    raw = str(token or "").strip()
    parts = raw.split(":")

    if len(parts) == 3:
        proto, port, scope = parts
    elif len(parts) == 4:
        proto = parts[0]
        port = parts[1] + ":" + parts[2]
        scope = parts[3]
    else:
        return None

    proto = proto.lower().strip()
    port = normalize_port(port)
    scope = scope.lower().strip()

    if proto not in {"tcp", "udp"}:
        return None

    if scope not in {"public", "lan", "localhost"}:
        return None

    if not valid_port(port):
        return None

    return proto, port, scope

def valid_ip_or_cidr(value: str) -> bool:
    if not value:
        return True
    try:
        if "/" in value:
            ipaddress.ip_network(value, strict=False)
        else:
            ipaddress.ip_address(value)
        return True
    except Exception:
        return False


def payload_dict(job: dict) -> dict:
    p = job.get("payload")
    if not p:
        return {}
    if isinstance(p, dict):
        return p
    try:
        return json.loads(p)
    except Exception:
        return {}


def _is_ipv6(ip: str) -> bool:
    try:
        obj = ipaddress.ip_network(ip, strict=False) if "/" in ip else ipaddress.ip_address(ip)
        return obj.version == 6
    except Exception:
        return False


def _iptables_bin(ip: str) -> str:
    return "ip6tables" if _is_ipv6(ip) else "iptables"


def firewall_block(ip: str) -> str:
    if not valid_ip_or_cidr(ip) or not ip:
        raise RuntimeError("Neplatná IP/CIDR.")
    bin = _iptables_bin(ip)
    chain = "ORISBLOCK"
    run([bin, "-N", chain], check=False)
    rc, _ = run([bin, "-C", chain, "-s", ip, "-j", "DROP"], check=False)
    if rc != 0:
        run([bin, "-A", chain, "-s", ip, "-j", "DROP"])
    rc, _ = run([bin, "-C", "INPUT", "-j", chain], check=False)
    if rc != 0:
        run([bin, "-I", "INPUT", "-j", chain])
    return f"{bin}: {ip} přidáno do ORISBLOCK"


def firewall_unblock(ip: str) -> str:
    if not valid_ip_or_cidr(ip) or not ip:
        raise RuntimeError("Neplatná IP/CIDR.")
    bin = _iptables_bin(ip)
    run([bin, "-D", "ORISBLOCK", "-s", ip, "-j", "DROP"], check=False)
    return f"{bin}: {ip} odebráno z ORISBLOCK"


def _valid_jail(name: str) -> bool:
    return bool(re.match(r"^[A-Za-z0-9_.:-]+$", name or ""))


