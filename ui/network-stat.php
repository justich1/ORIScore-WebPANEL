          </tr>
          <!-- rows generated by JS -->
        </table>
        <small><?=h(t('network.stats.help', [], 'RX/TX jsou rychlosti (průměr mezi posledními dvěma vzorky). “lo” je typicky lokální loopback.'))?></small>
