    <div class="row" style="gap:10px; margin-top:10px; flex-wrap:wrap">
      <span class="pill" id="pub4" style="display:none"><?=h(t('network.public_ipv4_empty', [], 'Veřejná IPv4: —'))?></span>
      <span class="pill" id="pub6" style="display:none"><?=h(t('network.public_ipv6_empty', [], 'Veřejná IPv6: —'))?></span>
    </div>

    <div style="margin-top:12px">
      <div class="table-scroll">
        <table id="ifaceTable">
          <tr>
            <th><?=h(t('network.col.interface', [], 'Interface'))?></th>
            <th><?=h(t('common.status', [], 'Stav'))?></th>
            <th><?=h(t('network.col.mac', [], 'MAC'))?></th>
            <th><?=h(t('network.col.ipv4', [], 'IPv4'))?></th>
            <th><?=h(t('network.col.ipv6', [], 'IPv6'))?></th>
          </tr>
          <!-- rows generated by JS -->
        </table>
        <small><?=t('network.ifaces.help', [], 'Adresy jsou vypsané přímo z <code>ip addr</code>. Pokud IPv6 na serveru není, sloupec zůstane prázdný.')?></small>
